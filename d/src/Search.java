 public void ShowRecord()
    {
                try
                {
                    MyCon();
                    sql="Select * From Student";
                    pst=cn.prepareStatement(sql);
                    rs=pst.executeQuery();
                    while(rs.next())
                    {
                        jTable1.getModel().setValueAt(rs.getInt("Rollno"),i, 0);
                         jTable1.getModel().setValueAt(rs.getString("Name"),i, 1);
                          jTable1.getModel().setValueAt(rs.getString("Address"),i, 2);
                          i++;
                    }
                    rs.close();
                    cn.close();
                            
                }
                            catch(Exception e)
                            {
                            JOptionPane.showMessageDialog(this, e);
                            }
    }
